def _render_section(manifest_df: pd.DataFrame, category: str, title: str) -> str:
    if manifest_df.empty:
        rows = manifest_df
    else:
        rows = manifest_df[manifest_df["category"] == category].copy()
    if rows.empty:
        return f"<section><h2>{escape(title)}</h2><div class=\"empty\">No artifacts were generated for this section.</div></section>"
    if category == "tables":
        items = "".join(
            f"<li><a href=\"{escape(row.report_path)}\">{escape(row.title)}</a> <span class=\"tagrow\">({escape(row.format)})</span></li>"
            for row in rows.itertuples(index=False)
        )
        return f"<section><h2>{escape(title)}</h2><ul class=\"table-list\">{items}</ul></section>"
    cards = "".join(_render_artifact_card(row) for row in rows.itertuples(index=False))
    return f"<section><h2>{escape(title)}</h2><div class=\"gallery\">{cards}</div></section>"


def _render_artifact_card(row) -> str:
    link = escape(row.report_path)
    if row.format in {"png", "jpg", "jpeg", "svg"}:
        media = f"<a href=\"{link}\"><img src=\"{link}\" loading=\"lazy\" alt=\"{escape(row.title)}\"></a>"
    else:
        media = f"<div class=\"empty\"><a href=\"{link}\">Open {escape(row.format.upper())}</a></div>"
    tags = [tag for tag in (row.sample, row.chromosome, row.source_path) if tag]
    tag_text = " | ".join(tags)
    return (
        f"<article class=\"artifact\">{media}<div class=\"body\">"
        f"<h3><a href=\"{link}\">{escape(row.title)}</a></h3>"
        f"<p>{escape(row.caption)}</p>"
        f"<div class=\"tagrow\">{escape(tag_text)}</div>"
        f"</div></article>"
    )


def _fmt_int(value: int | np.integer) -> str:
    return f"{int(value):,}"


def read_manifest(path: str | Path) -> pd.DataFrame:
    """Read a plot manifest written by :func:`write_plot_report`."""
    return pd.read_csv(path, sep="\t")


def iter_category(manifest_df: pd.DataFrame, category: str) -> Iterable[pd.Series]:
    """Yield manifest rows for a category in report order."""
    for _, row in manifest_df[manifest_df["category"] == category].iterrows():
        yield row
